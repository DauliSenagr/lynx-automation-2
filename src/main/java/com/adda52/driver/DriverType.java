/**
 * @author Dauli Senhgar
 * @since 15th May 2022
 * Enum representing different types of drivers or platforms for automation.
 * Contains options for CHROME, FIREFOX, ANDROID, and IOS.
 */
public enum DriverType {
    CHROME,    // Represents the Chrome browser driver
    FIREFOX,   // Represents the Firefox browser driver
    ANDROID,   // Represents the Android platform driver
    IOS        // Represents the iOS platform driver
}
